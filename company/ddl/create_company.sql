  DEFAULT CHARSET = utf8mb4
  COLLATE = utf8mb4_unicode_ci;

CREATE TABLE `DEPT_LOCATIONS`
(
    `Dnumber`   int(11)     NOT NULL,
    `Dlocation` varchar(15) NOT NULL,
    PRIMARY KEY (`Dnumber`, `Dlocation`),
    CONSTRAINT `DNUM_FK` FOREIGN KEY (`Dnumber`) REFERENCES `DEPARTMENT` (`Dnumber`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE = InnoDB
  DEFAULT CHARSET = utf8mb4
  COLLATE = utf8mb4_unicode_ci;

CREATE TABLE `PROJECT`
(
    `Pname`     varchar(30) NOT NULL,
    `Pnumber`   int(11)     NOT NULL,
    `Plocation` varchar(30) DEFAULT NULL,
    `Dnum`      int(11)     NOT NULL,
    PRIMARY KEY (`Pnumber`),
    KEY `PROJ_DEP_FK` (`Dnum`),
    CONSTRAINT `PROJ_DEP_FK` FOREIGN KEY (`Dnum`) REFERENCES `DEPARTMENT` (`Dnumber`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE = InnoDB
  DEFAULT CHARSET = utf8mb4
  COLLATE = utf8mb4_unicode_ci;

CREATE TABLE `WORKS_ON`
(
    `Essn`  char(9) CHARACTER SET ascii NOT NULL,
    `Pno`   int(11)                     NOT NULL,
    `Hours` decimal(3, 1)               NOT NULL,
    PRIMARY KEY (`Essn`, `Pno`),
    KEY `WKS_PROJ_FK` (`Pno`),
    CONSTRAINT `WKS_PROJ_FK` FOREIGN KEY (`Pno`) REFERENCES `PROJECT` (`Pnumber`) ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT `WKS_SSN_FK` FOREIGN KEY (`Essn`) REFERENCES `EMPLOYEE` (`Ssn`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE = InnoDB
  DEFAULT CHARSET = utf8mb4
  COLLATE = utf8mb4_unicode_ci;

CREATE TABLE `DEPENDENT`
(
    `Essn`           char(9) CHARACTER SET ascii NOT NULL,

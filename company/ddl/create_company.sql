    CONSTRAINT `DEP_SSN_FK` FOREIGN KEY (`Essn`) REFERENCES `EMPLOYEE` (`Ssn`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE = InnoDB
  DEFAULT CHARSET = utf8mb4
  COLLATE = utf8mb4_unicode_ci;



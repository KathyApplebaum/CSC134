    UNIQUE KEY `dname_unq` (`Dname`),
    CONSTRAINT `DEPTMGR_FK` FOREIGN KEY (`Mgr_ssn`) REFERENCES `EMPLOYEE` (`Ssn`) ON DELETE SET NULL ON UPDATE SET NULL

-- we'll alter the table later to add a couple of constraints after we load the data.

CREATE TABLE `DEPARTMENT`
(
    `Dname`        varchar(30) NOT NULL,
    `Dnumber`      int(11)     NOT NULL,
    `Mgr_ssn`       char(9) CHARACTER SET ascii DEFAULT NULL,
    `Mgr_start_date` date                        DEFAULT NULL,
